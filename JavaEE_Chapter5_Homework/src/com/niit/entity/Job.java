package com.niit.entity;

public class Job {
	
	private String job_id;
	private String job_title;
	
	public Job(){
		
	}
	
	public String getJob_id() {
		return job_id;
	}
	public void setJob_id(String jobId) {
		job_id = jobId;
	}
	public String getJob_title() {
		return job_title;
	}
	public void setJob_title(String jobTitle) {
		job_title = jobTitle;
	}
	
	

}
